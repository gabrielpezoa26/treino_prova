#include <unistd.h>

static void ft_putnbr(int n)
{
	char number;

	if (n >= 10)
		ft_putnbr(n / 10);
	number = (n % 10) + '0';
	write(1, &number, 1);
}

int	main(void)
{
	int i;

	i = 1;
	while (i <= 100)
	{
		if (i % 15 == 0)
			write(1, "fizzbuzz", 8);
		else if (i % 3 == 0)
			write(1, "fizz", 4);
		else if(i % 5 == 0)
			write(1, "buzz", 4);
		else
			ft_putnbr(i);
		i++;
		write(1, "\n", 1);
	}
}
