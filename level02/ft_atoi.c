#include <stdio.h>

int	ft_atoi(const char *str)
{
	int	i;
	int	mango_loko;
	int	signal;

	i = 0;
	mango_loko = 0;
	signal = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
	{
		i++;
	}
	if (str[i] == '-')
	{
		signal = (-1);
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		mango_loko = mango_loko * 10 + (str[i] - '0');
		i++;
	}
	return (mango_loko * signal);
}

int main(void)
{
	char *potato = "  -863";

	printf("%d\n", ft_atoi(potato));
}
